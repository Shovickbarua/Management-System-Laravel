     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'sem_name' => 'required'
        ]);
        $semester = new Semester();
        $semester->sem_name = $request->sem_name;
        $semester->save();

        return redirect(route('semester.index'));
    }

